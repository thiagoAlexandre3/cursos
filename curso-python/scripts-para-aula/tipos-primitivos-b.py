n = input("Digite um valor: ");
print(type(n));
#mostra qual o tipo da variavel
#algumas funções:

print(n.isnumeric());
#retorna o valor boleano true, se der para converter essa string, para um número
print(n.isalpha());
#retorna o valor boleano true, se a variavel for composta de apenas letras
print(n.isalnum());
#retorna o valor boleano true, se a variavel for composta por letras ou numeros
print(n.isupper());
#retorna o valor boleano true, se a variavel for composta por letras maisculas
print(n.islower());
#retorna o valor boleano true, se a variavel for composta por letras minusculas