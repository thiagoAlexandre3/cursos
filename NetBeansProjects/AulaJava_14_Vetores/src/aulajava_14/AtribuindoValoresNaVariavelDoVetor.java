package aulajava_14;

public class AtribuindoValoresNaVariavelDoVetor {

    public static void main(String[] args) {
        int v[] = {4, 6, 2, 0, 7, 8};
        for (int c = 0; c < v.length; c++) { // length, significa o comprimento em que o vetor possue
            System.out.println("Na posição " + c + ", temos o valor: " + v[c]);
        }

    }
}
